var purlist = ['banana', 'mango', 'orange'];
console.log(purlist);
purlist.push('pineapple'); //add an element to the last
console.log(purlist);
purlist.unshift('grapes'); //add element to the first
console.log(purlist);
purlist.pop(); //remove the last element
console.log(purlist);
purlist.shift(); //remove the first element
console.log(purlist);
